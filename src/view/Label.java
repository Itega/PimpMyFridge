package view;

import javax.swing.*;
import java.awt.*;

/**
 * The type Label.
 */
public class Label extends JLabel{

    /**
     * Instantiates a new Label.
     */
    public Label() {

        this.setFont(new Font("Arial",Font.BOLD,30));
        this.setForeground(Color.decode("#0077AC"));
    }
}
